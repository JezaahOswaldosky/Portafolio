#### Programa3.py es un codigo que permite al usuario calcular
#### el area de un rectangulo. El usuario solo debe introducir 
#### la base y altura, para que el programa otorgue el area. 
#### 1. Pide altura al usuario 
#### 2. Pide base al usuario 
